# 🛠 ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
def safe_sleep():
    """Безопасная задержка с джиттером для обхода детекторов и соблюдения лимитов"""
    delay = DELAY_BASE + random.uniform(0, DELAY_JITTER)
    time.sleep(delay)

def parse_yandex_response(data: dict) -> dict:
    """Извлекает данные из ответа Яндекс.Геокодера"""
    members = data.get("response", {}).get("GeoObjectCollection", {}).get("featureMember", [])
    if not members:
        return {"found": False, "coords": None, "address": None, "kind": None}
    
    obj = members[0]["GeoObject"]
    lon, lat = map(float, obj["Point"]["pos"].split())
    kind = obj.get("metaDataProperty", {}).get("GeocoderMetaData", {}).get("kind")
    name = obj.get("name", "")
    return {
        "found": True,
        "coords": f"{lat:.6f},{lon:.6f}",
        "address": name,
        "kind": kind
    }

def query_yandex(session: requests.Session, params: dict) -> Optional[dict]:
    """Запрос с ретраями и обработкой 429/5xx"""
    for attempt in range(MAX_RETRIES):
        try:
            resp = session.get(BASE_URL, params=params, timeout=TIMEOUT)
            if resp.status_code == 429:
                wait = (2 ** attempt) + random.uniform(0, 1)
                logging.warning(f" Rate Limit (429). Пауза {wait:.1f}с...")
                time.sleep(wait)
                continue
            if resp.status_code >= 500:
                wait = (2 ** attempt) + random.uniform(0, 1)
                logging.warning(f"🌐 Серверная ошибка {resp.status_code}. Пауза {wait:.1f}с...")
                time.sleep(wait)
                continue
            
            resp.raise_for_status()
            return resp.json()
        except requests.exceptions.RequestException as e:
            logging.error(f"❌ Ошибка сети: {e}")
            time.sleep(1)
    return None

# 🚀 ОСНОВНОЙ ПРОЦЕСС
def run_benchmark():
    if API_KEY == "ВСТАВЬТЕ_ВАШ_КЛЮЧ_ЯНДЕКС":
        logging.error("🔑 Укажите API ключ в переменной YANDEX_API_KEY или в начале скрипта!")
        return

    session = requests.Session()
    session.headers.update({"User-Agent": "GeocodeBenchmark/1.0"})
    
    results = []
    total = len(ADDRESSES) + len(COORDS)
    logging.info(f"🚀 Начало тестирования. Всего запросов: {total}")
    logging.info(f"⏱ Тротлинг: ~{DELAY_BASE+DELAY_JITTER/2:.1f}с между запросами")

    # 1. Прямое геокодирование
    for i, addr in enumerate(ADDRESSES, 1):
        params = {"apikey": API_KEY, "geocode": addr, "format": "json", "results": 1}
        logging.info(f"📍 [{i}/{total}] Прямой: {addr[:50]}...")
        
        start = time.perf_counter()
        data = query_yandex(session, params)
        elapsed = (time.perf_counter() - start) * 1000
        
        parsed = parse_yandex_response(data) if data else {"found": False, "coords": None, "address": None, "kind": None}
        results.append({
            "type": "direct", "query": addr, "found": parsed["found"],
            "result_coords": parsed["coords"], "result_address": parsed["address"],
            "yandex_kind": parsed["kind"], "time_ms": round(elapsed, 1),
            "error": "API Error" if data is None else ""
        })
        safe_sleep()

    # 2. Обратное геокодирование (ВНИМАНИЕ: Яндекс требует lon,lat)
    for i, (lat, lon) in enumerate(COORDS, 1 + len(ADDRESSES)):
        yandex_geocode_str = f"{lon},{lat}"  # Swap!
        params = {"apikey": API_KEY, "geocode": yandex_geocode_str, "format": "json", "results": 1}
        logging.info(f"🔄 [{i}/{total}] Обратный: {yandex_geocode_str}")
        
        start = time.perf_counter()
        data = query_yandex(session, params)
        elapsed = (time.perf_counter() - start) * 1000
        
        parsed = parse_yandex_response(data) if data else {"found": False, "coords": None, "address": None, "kind": None}
        results.append({
            "type": "reverse", "query": yandex_geocode_str, "found": parsed["found"],
            "result_coords": parsed["coords"], "result_address": parsed["address"],
            "yandex_kind": parsed["kind"], "time_ms": round(elapsed, 1),
            "error": "API Error" if data is None else ""
        })
        safe_sleep()

    # 💾 Сохранение в CSV
    with open(OUTPUT_CSV, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=results[0].keys())
        writer.writeheader()
        writer.writerows(results)
    
    logging.info(f"✅ Готово! Результаты сохранены в {OUTPUT_CSV}")
    logging.info(f"📊 Статистика: Найдено {sum(1 for r in results if r['found'])}/{len(results)} ({sum(1 for r in results if r['found'])/len(results)*100:.1f}%)")

if __name__ == "__main__":
    run_benchmark()